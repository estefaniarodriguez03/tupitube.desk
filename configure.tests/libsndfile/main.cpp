#include <stdio.h>
#include <string.h>
#include "sndfile.h"

int main()
{
    SNDFILE *infile;
    SF_INFO sfinfo;
    const char *filename = "input.mp3";

    if (!(infile = sf_open(filename, SFM_WRITE, &sfinfo))) {
        printf ("Not able to open input file %s.\n", filename) ;
        puts (sf_strerror (NULL)) ;
        sf_close (infile) ;
	return 1 ;
    }

    /* Close file. */
    sf_close(infile) ;
}
